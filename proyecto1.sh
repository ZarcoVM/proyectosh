do
  fecha="${item%%|*}"
  cantidad="${item##*|}"

  for i in $(seq 1 $cantidad)
  do
    hora=$(printf "%02d" $((8 + (i % 10))))
    minuto=$(printf "%02d" $((i * 3 % 60)))
    segundo=$(printf "%02d" $((i * 7 % 60)))
    export GIT_AUTHOR_DATE="$fecha $hora:$minuto:$second:00"
    export GIT_COMMITTER_DATE="$fecha $hora:$minuto:$second:00"

    echo "Commit $i para $fecha" >> archivo.txt
    git add archivo.txt
    git commit -m "Commit $i para $fecha"
  done
done

